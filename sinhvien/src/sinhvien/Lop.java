package sinhvien;

public class Lop {

    private String maLop;
    private String tenLop;

    public Lop(String maLop, String tenLop) {
        this.maLop = maLop;
        this.tenLop = tenLop;
    }

    public String getMalop() {
        return maLop;
    }

    public void setMalop(String maLop) {
        this.maLop = maLop;
    }

    public String getTenlop() {
        return tenLop;
    }

    public void setTenlop(String tenLop) {
        this.tenLop = tenLop;
    }
    
}
